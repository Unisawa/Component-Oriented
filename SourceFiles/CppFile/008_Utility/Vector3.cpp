/*===============================================================================================* 
  @Summary: ベクトルの正規化を行う
  @Details: None
 *===============================================================================================*/
void Vector3::Normalize()
{
    float Magnitude = sqrtf((x * x) + (y * y) + (z * z));

    if (Magnitude != 0)
    {
        Magnitude = 1 / Magnitude;

        x = x * Magnitude;
        y = y * Magnitude;
        z = z * Magnitude;
    }
    else
    {
        x = 0.0f;
        y = 0.0f;
        z = 0.0f;
    }
}

