COLOR Color::Trans()
{
    COLOR color = 0;

    color = ( (COLOR)  ( ((((long)(a * 255.0f)) & 0xff) << 24) | ((((long)(r * 255.0f)) & 0xff) << 16) | ((((long)(g * 255.0f)) & 0xff) << 8) | ((((long)(b * 255.0f)) & 0xff))) );

    return color;
}

