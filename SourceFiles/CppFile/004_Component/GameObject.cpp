/*=================================================================================================
  @Summary: コンストラクタ
  @Details: None
=================================================================================================*/
GameObject::GameObject(std::string name)
{
    activeSelf = true;
    transform  = NULL;

    SetName(name);
}

/*===============================================================================================* 
  @Summary: デストラクタ
  @Details: None
 *===============================================================================================*/
GameObject::~GameObject()
{
    Component* pComponent;

    for (auto Iterator = componentList.begin(); Iterator != componentList.end();)
    {
        pComponent = (*Iterator);

        // コンポーネントの削除
        SafeDeleteUninit(pComponent);

        Iterator++;
    }

    componentList.clear();

    SafeDelete(transform);
}

