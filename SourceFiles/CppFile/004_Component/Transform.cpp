/*=================================================================================================
  @Summary: コンストラクタ
  @Details: None
=================================================================================================*/
Transform::Transform()
{
    parent = NULL;

    position.x = 0.0f;
    position.y = 0.0f;
    position.z = 0.0f;

    rotation.x = 0.0f;
    rotation.y = 0.0f;
    rotation.z = 0.0f;

    lossyScale.x = 1.0f;
    lossyScale.y = 1.0f;
    lossyScale.z = 1.0f;
}

/*===============================================================================================* 
  @Summary: デストラクタ
  @Details: None
 *===============================================================================================*/
Transform::~Transform()
{

}

/*===============================================================================================* 
  @Summary: 
  @Details: 
 *===============================================================================================*/
void Transform::SetParent(Transform& value)
{
    this->parent = &value;
}

